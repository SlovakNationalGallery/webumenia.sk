                        <div class="col-md-6 attributes">
                            <h3>Archiv negativů Dagmar Hochové</h3>
                            <p>
                                V letech 2020–2024 Moravská galerie v Brně ve spolupráci s Ústavem pro soudobé dějiny Akademie věd ČR a Kanceláří Poslanecké sněmovny Parlamentu ČR realizovala úplnou digitalizaci a odborné zpracování souboru negativů Dagmar Hochové.
                                Projekt „Demokratická revoluce objektivem Dagmar Hochové: Zpřístupnění unikátní fotodokumentace z let 1989–1992“ byl spolufinancován se státní podporou Technologické agentury ČR v rámci Programu ÉTA.
                            </p>
                            <p>
                                Část obsahově identifikovaných snímků dokumentujících utváření demokratické politiky po listopadu 1989 je zveřejněn jako <a href="https://sbirky.moravska-galerie.cz/katalog?related_work=Archiv+negativ%C5%AF+Dagmar+Hochov%C3%A9">Archiv negativů Dagmar Hochové</a>. Soubor je unikátní rozsahem i významem pro poznání veřejného života a politického vývoje. Obrázky jsou snímané z poslanecké lavice a dalších míst vyhrazených ne fotografům, ale politickým aktérům. „Myslím, že ocenění toho souboru teprve časem přijde,“ předpovídala Hochová ve vzpomínkové knize Narodila jsem se za bouřky (2008), v níž vyslovila přání, aby snímky jednou posloužily jako historický pramen ke studiu demokratické revoluce. Spolu s ní věříme, že najdou mnohostranné využití při vědeckých a osvětových projektech i jako pomůcka ve výuce.
                            </p>
                            <p>
                                Naprostá většina snímků je zveřejněna poprvé a soubor se bude nadále rozšiřovat. Odborné zpracovávání fondu pokračuje a údaje jsou průběžně aktualizovány. Vaše připomínky a doplňující informace k fotografiím můžete zasílat na adresu <a href="mailto:jiri.patek@moravska-galerie.cz">jiri.patek@moravska-galerie.cz</a>.
